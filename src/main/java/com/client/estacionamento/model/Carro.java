package com.client.estacionamento.model;

public class Carro {

    private String marca;
    private String palca;

    public Carro() {
    }

    public String getPalca() {
        return palca;
    }

    public void setPalca(String palca) {
        this.palca = palca;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }
}
